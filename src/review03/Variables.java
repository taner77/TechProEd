package review03;

public class Variables {
    public static void main(String[] args) {
        //int ve long data type'nin max ve min degerlerini bulunuz

        int maxValue = Integer.MAX_VALUE;
        System.out.println(maxValue); //2147483647

        int minValue = Integer.MIN_VALUE;
        System.out.println(minValue); //-2147483648

        long minV = Long.MIN_VALUE; //-9223372036854775808
        System.out.println(minV);

        long maxV = Long.MAX_VALUE;
        System.out.println(maxV); //9223372036854775807

        long tlfLng = 535_300_3030L;
        System.out.println(tlfLng);


    }
}
