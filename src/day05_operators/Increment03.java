package day05_operators;

public class Increment03 {
    public static void main(String[] args) {
        // int data type'inda iki tane variable olustutrun
        // Bu vaiable'lari en kisa yoldan 1 artirin
        // NOT: En kisa yol sadece 1 artirmak icin kullanilir

        int sayi1 =12;
        sayi1++;
        System.out.println(sayi1);


        int sayi2 = 20;
        sayi2++; // variable isminden sonra iki kere + smebolu yazarsaniz
                 // o variable'in degerini 1 artirmis olursunuz
        System.out.println(sayi2);


    }
}
