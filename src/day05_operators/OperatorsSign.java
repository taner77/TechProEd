package day05_operators;

public class OperatorsSign {
    public static void main(String[] args) {

        int num1 = 12;
        int num2 = 20;

        System.out.println(num1==num2);
        System.out.println(num1<num2);
        System.out.println(num1!=num2);
        System.out.println(num1>=num2);

    }
}
